// POWER FUNCTION & ROOT FUNCTION IN C++ PROGRAMMING
#include <iostream>
#include <cmath>
int main () {
    std::cout << "Power Function: " << pow(2.0f, 4.0f) << std::endl;
    std::cout << "Power Function: " << pow(3.0f, 8.0f) << std::endl;

    std::cout << "Root Function: " << pow(9.0f, 1/2.0f) << std::endl;
    std::cout << "Root Function: " << pow(281.0f, 1/3.0f) << std::endl;
    return 0;
}